

public class SurfaceArea
{
    static float circleArea(float r){
        float area;
        area=3.14f*r*r;
        
        return area;
        
        
    
}
static float RectangleArea(float a, float b){
        float area;
        area=a*b;
        
        return area;
    
}
static float TrialangleArea(float a, float h){
        float area;
        area=0.5f*a*h;
        
        return area;
    
}

}    
